-- =========================================================
-- 001_schema.sql
-- Схема БД системы учёта заявок сотрудников.
-- СУБД: PostgreSQL 16.
-- =========================================================

-- Нужно для gen_random_uuid() в seed-скриптах и в приложении
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- =========================================================
-- Employees — справочник сотрудников.
--
-- Department и Position хранятся как атрибуты сотрудника,
-- а не в отдельных справочных таблицах: в рамках требований
-- задачи это простые строковые характеристики без собственного
-- жизненного цикла (нет ни атрибутов, ни связей, специфичных
-- только для отдела/должности). Вынос в отдельные таблицы
-- дал бы избыточную нормализацию без практической пользы
-- в границах текущих требований.
-- =========================================================
CREATE TABLE Employees (
    Id          UUID PRIMARY KEY,
    FullName    VARCHAR(200) NOT NULL,
    Department  VARCHAR(150) NOT NULL,
    Position    VARCHAR(150) NOT NULL
);

-- =========================================================
-- Tickets — заявки.
--
-- AuthorId / ExecutorId — внешние ключи на Employees, а не
-- дублирование ФИО/подразделения прямо в заявке. Это устраняет
-- избыточность данных и удовлетворяет 3NF: любое изменение ФИО
-- или подразделения сотрудника отражается во всех его заявках
-- автоматически, без массового обновления таблицы Tickets.
--
-- ExecutorId нанллабелен: заявка может быть создана без
-- назначенного исполнителя (см. CreateTicketDto.ExecutorId : Guid?).
--
-- Status хранится как SMALLINT (0=New, 1=InProgress, 2=Completed),
-- а не текстовым полем и не отдельной таблицей-справочником:
-- набор статусов фиксирован, мал и не меняется во время работы
-- системы, а ответственность за допустимые переходы между
-- статусами и так уже лежит в domain-слое (State pattern),
-- а не в БД. Хранение числом компактнее и позволяет строить
-- по нему эффективный составной индекс.
--
-- Number — человекочитаемый уникальный номер заявки (например,
-- "T-1001"), генерируется через SEQUENCE (см. ticket_number_seq
-- ниже) на уровне приложения (TicketNumberGenerator).
-- =========================================================
CREATE TABLE Tickets (
    Id          UUID PRIMARY KEY,
    Number      VARCHAR(50) NOT NULL UNIQUE,
    CreatedAt   TIMESTAMPTZ NOT NULL,
    Description TEXT NOT NULL,
    Deadline    TIMESTAMPTZ NOT NULL,
    AuthorId    UUID NOT NULL REFERENCES Employees(Id),
    ExecutorId  UUID NULL     REFERENCES Employees(Id),
    Status      SMALLINT NOT NULL CHECK (Status IN (0, 1, 2))
);

-- Последовательность для генерации номеров заявок (см.
-- TicketNumberGenerator в Infrastructure-слое). Атомарна на
-- уровне СУБД, не требует блокировки таблицы Tickets при
-- конкурентной вставке заявок.
CREATE SEQUENCE ticket_number_seq START 1;

-- =========================================================
-- Индексы
-- =========================================================

-- Под целевой запрос из требований: "просроченные заявки
-- конкретного исполнителя в статусе В работе, отсортированные
-- по сроку выполнения". Порядок колонок: сначала равенство
-- (ExecutorId, Status), затем колонка сортировки/диапазона
-- (Deadline) — индекс покрывает и фильтрацию, и ORDER BY.
CREATE INDEX IX_Tickets_Executor_Status_Deadline
    ON Tickets (ExecutorId, Status, Deadline);

-- Под фильтрацию списка заявок по подразделению исполнителя
-- (GetFiltered с параметром department) — ускоряет JOIN Tickets
-- -> Employees по ExecutorId с последующим фильтром по Department.
CREATE INDEX IX_Employees_Department
    ON Employees (Department);