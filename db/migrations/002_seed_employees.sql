INSERT INTO Employees (Id, FullName, Department, Position)
SELECT
    gen_random_uuid(),
    'Сотрудник ' || i,
    (ARRAY['Разработка','Поддержка','Продажи','Бухгалтерия','HR'])[1 + (i % 5)],
    (ARRAY['Специалист','Ведущий специалист','Руководитель отдела'])[1 + (i % 3)]
FROM generate_series(1, 1000) AS i;